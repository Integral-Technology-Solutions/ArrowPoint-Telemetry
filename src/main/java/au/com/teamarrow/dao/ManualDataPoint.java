package au.com.teamarrow.dao;

public class ManualDataPoint {

	String dataPointOptions;
	String dataPoint;
	String fval;
	public String getDataPointOptions() {
		return dataPointOptions;
	}
	public void setDataPointOptions(String dataPointOptions) {
		this.dataPointOptions = dataPointOptions;
	}
	public String getDataPoint() {
		return dataPoint;
	}
	public void setDataPoint(String dataPoint) {
		this.dataPoint = dataPoint;
	}
	public String getFval() {
		return fval;
	}
	public void setFval(String fval) {
		this.fval = fval;
	}
	
	
	
}
